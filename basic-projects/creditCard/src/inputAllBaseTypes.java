public class inputAllBaseTypes
{
    static void inputAllTypes()
    {
        short tia = 3;
        byte ok = 32;
        int num = 100;
        long longy = 1000l;
        double multi = 59.90;
        float fly = 23.5f;
        char baako = 'S';
        boolean ampa = true;

        System.out.println("Short: " + tia);
        System.out.println("Byte: " + ok);
        System.out.println("Int: " + num);
        System.out.println("Long: " + longy);
        System.out.println("Double: " + multi);
        System.out.println("Float: " + fly);
        System.out.println("Char: " + baako);
        System.out.println("Boolean: " + ampa);
    }

    public static void main(String[] args)
    {
        inputAllTypes();
    }
}
